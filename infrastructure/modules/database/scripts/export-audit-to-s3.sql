-- FRECUENCIA: Cada hora (EventBridge schedule)
-- DESTINO: s3://brainmart-{env}-audit-logs/year=YYYY/month=MM/day=DD/hour=HH/
-- FORMATO: Parquet (columnar, comprimido, eficiente para Athena)
--
-- ALCOA+:
--   L (Legible): Parquet es esta?ndar de industria, Athena lo puede leer
--   A (Available): S3 tiene 99.999999999% durabilidad
--   E (Enduring): S3 Object Lock GOVERNANCE 7 an?os
