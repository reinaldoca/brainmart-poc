-- Obtener los registros de la u?ltima hora que au?n no han sido exportados
-- La columna exported_at = NULL indica que no han sido exportados
SELECT
    id::TEXT                    AS audit_id,
    schema_name                 AS schema_name,
    table_name                  AS table_name,
    record_id                   AS record_id,
    operation                   AS operation,
    CASE operation
        WHEN 'I' THEN 'INSERT'
        WHEN 'U' THEN 'UPDATE'
        WHEN 'D' THEN 'DELETE'
    END                         AS operation_name,
    changed_by                  AS changed_by,
    application_user            AS application_user,
    ip_address::TEXT            AS ip_address,
    session_id                  AS session_id,
    changed_at                  AS changed_at,
    changed_at AT TIME ZONE 'UTC' AS changed_at_utc,
    DATE(changed_at AT TIME ZONE 'UTC') AS partition_date,
    EXTRACT(YEAR  FROM changed_at AT TIME ZONE 'UTC')::INT AS partition_year,
    EXTRACT(MONTH FROM changed_at AT TIME ZONE 'UTC')::INT AS partition_month,
    EXTRACT(DAY   FROM changed_at AT TIME ZONE 'UTC')::INT AS partition_day,
    EXTRACT(HOUR  FROM changed_at AT TIME ZONE 'UTC')::INT AS partition_hour,
    transaction_id::TEXT        AS transaction_id,
    old_values::TEXT            AS old_values_json,
    new_values::TEXT            AS new_values_json,
    changed_fields              AS changed_fields,
    request_id                  AS request_id,
    correlation_id              AS correlation_id,
    integrity_hash              AS integrity_hash,
    -- Calculamos si el hash de integridad es va?lido en el momento de la exportacio?n
    (integrity_hash = encode(
        digest(
            CONCAT(
                table_name, '|',
                record_id, '|',
                operation, '|',
                changed_at::TEXT, '|',
                COALESCE(new_values::TEXT, old_values::TEXT, '')
            )::BYTEA,
            'sha256'
        ),
        'hex'
    ))                          AS integrity_valid,
    NOW() AT TIME ZONE 'UTC'    AS exported_at
FROM
    audit_log
WHERE
    -- Solo exportar registros que no han sido exportados au?n
    exported_at IS NULL
    -- Solo registros de la hora pasada (para evitar exportar demasiado)
    AND changed_at >= NOW() - INTERVAL '1 hour 5 minutes'
    AND changed_at < NOW() - INTERVAL '5 minutes'  -- 5 min de buffer para transacciones en vuelo
ORDER BY
    changed_at ASC,  -- Orden cronolo?gico para mantener el orden en Parquet
    table_name ASC;

